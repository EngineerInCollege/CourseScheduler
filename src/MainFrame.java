    private void scheduleClassButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_scheduleClassButtonActionPerformed
        String courseCode = (String) selectClassComboBox.getSelectedItem();
        ArrayList<StudentEntry> students = StudentQueries.getAllStudents();
        String studentID = null;
        String status = null;
        String studentName = (String) selectStudentScheduleComboBox.getSelectedItem();
        for (StudentEntry student : students)
        {
            if ((student.getLastName() + ", " + student.getFirstName()).equals(studentName))
            {
                studentID = student.getStudentID();
                break;
            }
        }
        int seats = ClassQueries.getClassSeats(currentSemester, courseCode);
        int currentFilledSeats = ScheduleQueries.getScheduledStudentCount(currentSemester, courseCode);
        if (currentFilledSeats + 1 > seats)
        {
            status = "W";
            scheduleClassStatusLabel.setText("Student " + studentName + " has been waitlisted for " + courseCode +".");
        }
        else
        {
            status = "S";
            scheduleClassStatusLabel.setText("Student " + studentName + " has been scheduled for " + courseCode +".");
        }
        java.sql.Timestamp currentTimestamp = new java.sql.Timestamp(Calendar.getInstance().getTime().getTime());
        ScheduleEntry scheduleEntry = new ScheduleEntry(currentSemester, courseCode, studentID, status, currentTimestamp);
        ScheduleQueries.addScheduleEntry(scheduleEntry);
    }//GEN-LAST:event_scheduleClassButtonActionPerformed

