    private void dropStudentSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dropStudentSubmitButtonActionPerformed
        ArrayList<StudentEntry> students = StudentQueries.getAllStudents();
        String studentID = null;
        String studentName = (String) dropStudentComboBox.getSelectedItem();
        for (StudentEntry student : students)
        {
            if ((student.getLastName() + ", " + student.getFirstName()).equals(studentName))
            {
                studentID = student.getStudentID();
                break;
            }
        }
        
        dropStudentTextArea.setText(studentName + " " + studentID + " has been dropped from the list of students.\n");
        ArrayList<String> semesters = SemesterQueries.getSemesterList();
        for (String semester : semesters)
        {
            dropStudentTextArea.append("\nFor Semester: " + semester + "\n");
            ArrayList<ScheduleEntry> schedules = ScheduleQueries.getScheduleByStudent(semester, studentID);
            for (ScheduleEntry s : schedules)
            {
                if (s.getStatus().equals("S"))
                {
                    dropStudentTextArea.append(studentName + " " + studentID + " has been dropped from " + s.getCourseCode() + "\n");
                    
                    ArrayList<ScheduleEntry> waitlistedStudentSchedules = ScheduleQueries.getWaitlistedStudentsByClass(semester, s.getCourseCode());
                    if (!waitlistedStudentSchedules.isEmpty())
                    {
                        ScheduleEntry updatedStudentSchedule = waitlistedStudentSchedules.get(0);
                        StudentEntry updatedStudent = StudentQueries.getStudent(updatedStudentSchedule.getStudentID());
                        ScheduleQueries.updateScheduleEntry(updatedStudentSchedule);
                        dropStudentTextArea.append(updatedStudent.getLastName() + ", " + updatedStudent.getFirstName() + " " + updatedStudent.getStudentID() + " has been scheduled into " + s.getCourseCode() + "\n");
                    }
                } else if (s.getStatus().equals("W"))
                {
                    dropStudentTextArea.append(studentName + " " + studentID + " has been dropped from the waitlist for " + s.getCourseCode() + "\n");
                }
                ScheduleQueries.dropStudentScheduleByCourse(semester, studentID, s.getCourseCode());
            }
        } 
        StudentQueries.dropStudent(studentID);
        rebuildStudentComboBoxes();
    }//GEN-LAST:event_dropStudentSubmitButtonActionPerformed

    private void adminDropClassSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adminDropClassSubmitButtonActionPerformed
        String courseCode = (String) adminDropClassClassComboBox.getSelectedItem();
        ArrayList<StudentEntry> scheduledStudents = MultiTableQueries.getScheduledStudentsByClass(currentSemester, courseCode);
        ArrayList<StudentEntry> waitlistedStudents = MultiTableQueries.getWaitlistedStudentsByClass(currentSemester, courseCode);
        
        adminDropClassTextArea.append("Scheduled students dropped from the course:\n");
        for (StudentEntry scheduledStudent : scheduledStudents)
        {
            ScheduleQueries.dropStudentScheduleByCourse(currentSemester, scheduledStudent.getStudentID(), courseCode);
            adminDropClassTextArea.append(scheduledStudent.getLastName() + ", " + scheduledStudent.getFirstName() + " " + scheduledStudent.getStudentID() + "\n");
        }
        
        adminDropClassTextArea.append("\nWaitlisted students dropped from the course:\n");
        for (StudentEntry waitlistedStudent : waitlistedStudents)
        {
            ScheduleQueries.dropStudentScheduleByCourse(currentSemester, waitlistedStudent.getStudentID(), courseCode);
            adminDropClassTextArea.append(waitlistedStudent.getLastName() + ", " + waitlistedStudent.getFirstName() + " " + waitlistedStudent.getStudentID() + "\n");
        }
        
        ClassQueries.dropClass(currentSemester, courseCode);
        rebuildClassCourseCodeComboBoxes();
    }//GEN-LAST:event_adminDropClassSubmitButtonActionPerformed

