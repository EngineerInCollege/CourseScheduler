    private void displayScheduleDisplayButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_displayScheduleDisplayButtonActionPerformed
        ArrayList<StudentEntry> students = StudentQueries.getAllStudents();
        String studentName = (String) selectStudentDisplayScheduleComboBox.getSelectedItem();
        for (StudentEntry student : students)
        {
            if ((student.getLastName() + ", " + student.getFirstName()).equals(studentName))
            {
                ArrayList<ScheduleEntry> schedules = ScheduleQueries.getScheduleByStudent(currentSemester, student.getStudentID());
                DefaultTableModel displayCoursesTableModel = (DefaultTableModel) displayScheduleTable.getModel();
        
                displayCoursesTableModel.setNumRows(0);
                Object[] rowData = new Object[3];
        
                for (ScheduleEntry s: schedules) {
                    rowData[0] = s.getCourseCode();
                    rowData[1] = s.getStatus();
                    displayCoursesTableModel.addRow(rowData);
                }
                
                break;
            }
        }
    }//GEN-LAST:event_displayScheduleDisplayButtonActionPerformed

    
